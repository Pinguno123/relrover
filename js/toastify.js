function loginError(text) {
  Toastify({
    text: text,
    duration: 3000,
    destination: "",
    newWindow: true,
    close: false,
    gravity: "top",
    position: "right",
    stopOnFocus: true,
    style: {
      background: "linear-gradient(to right, var(--background), var(--secondary))",
    },
    onClick: function(){}
  }).showToast();
}

